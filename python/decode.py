def decode_compact_ip_port(data):
    ip_addresses = []
    port_numbers = []

    # Iterate over the data, considering each compact IP address/port pair
    i = 0
    while i < len(data):
        # Extract the IP address (first 4 bytes)
        ip_bytes = data[i:i+4]
        # Extract the port number (next 2 bytes)
        port_bytes = data[i+4:i+6]

        # Convert bytes to integers
        ip_address = '.'.join(str(byte) for byte in ip_bytes)
        port_number = int.from_bytes(port_bytes, byteorder='big')

        ip_addresses.append(ip_address)
        port_numbers.append(port_number)

        # Move to the next compact IP address/port pair
        i += 6

    return ip_addresses, port_numbers

